function t(key, params = {}) {
    const lang = getCurrentLanguage();
    let text = translations[lang][key] || translations['ko'][key] || key;
    
    // íŒŒë¼ë¯¸í„° ì¹˜í™˜
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });
    
    return text;
}

